    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();

        if (selectedRowIndex != -1) {
            String id_pelanggan = model.getValueAt(selectedRowIndex, 0).toString(); // Ambil ID dari baris yang dipilih
            try {
                Connection kon = koneksi.koneksiDb();
                Statement st = kon.createStatement();
                String sql = "DELETE FROM pelanggan WHERE id_pelanggan='" + id_pelanggan + "'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                load_data(); // Setelah menghapus data, muat ulang tabel
                IDOtomatis(); // Perbarui ID otomatis
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectedRowIndex = jTable1.getSelectedRow();

    // Cek jika baris telah dipilih
    if (selectedRowIndex >= 0) {
        // Aktifkan tombol Update jika baris dipilih
        btnUpdate.setEnabled(true);

        // Ambil data dari baris yang dipilih
        String id_pelanggan = model.getValueAt(selectedRowIndex, 0).toString();
        String nama = model.getValueAt(selectedRowIndex, 1).toString();
        String alamat = model.getValueAt(selectedRowIndex, 2).toString();
        String email = model.getValueAt(selectedRowIndex, 3).toString();

        // Masukkan data ke dalam JTextField
        jTextField1.setText(id_pelanggan);
        jTextField2.setText(nama);
        jTextField3.setText(alamat);
        jTextField4.setText(email);
    }
    }//GEN-LAST:event_jTable1MouseClicked

